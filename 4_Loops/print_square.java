
public class print_square {
    public static void main(String[] args) {
        //-----for loop-----
        for(int line=1; line<=4; line++ ) {
            System.out.println("* * * *");
        }
        System.out.println();

        //-----while loop-----
        int lines = 1;
        while(lines <= 4){
            System.out.println("* * * *");
            lines++;

    }

    }
}
